<h3>Q. So... What?</h3>
<p><b>A.</b> Let's face it, it sucks to get the wrong gift. Ditzy mums, useless partners, interesting grandparents, and even sometimes your closest friends - Sometimes when it comes to presents they manage to go astray. And you've probably made a few mistakes in your lifetime too, even if you thought that look on your dad's face was genuine happiness.
Here at Gift Circle we're putting a stop to bad gifts. How? With simple, useable, fun wishlists that make everyone happy.</p>

<h3>Q. OK, so when's a good time to use Gift Circle?</h3>
<p><b>A.</b> Any time there's an upcoming occasion in which the exchange of gifts is expected!</p>

<h3>Q. Doesn't this take the surprise out of presents?</h3>
<p><b>A.</b> We don't like to judge. Besides, people regularly use wishlists for Weddings, so why not every event? 
At the end of the day there will always be some people who value the surprise. For those of us who just like receiving or giving cool stuff, there's Gift Circle.</p>

<h3>Q. Alright, I'm in. How can I let my friends and family know what I want?</h3>
<p><b>A.</b> If you're signed up, all you need to do is go to Dashboard and hit "New List". Give it a name and if you want, an expiry date. Add your gifts and add some friends and you're off!
If you're not signed up... What are you waiting for?</p>

<h3>Q. How can I get the right thing for my friends/family?</h3>
<p><b>A.</b> Head over to Dashboard and check out the section titled "Friends Lists". If a gift is in the list, it's up for grabs! Reserve it to make sure others know not to buy it, and then purchase it! Job done!</p>

<h3>Q. Let's get technical. How do I manage my created list?</h3>
<p><b>A.</b> Go to Dashboard and click on a list you've created. You can add or remove gifts, and add or remove friends from this list/circle right here! (More on this below)</p>

<h3>Q. Circles? Friends? How can I manage my acquaintances?</h3>
<p><b>A.</b> Here at Gift Circle you have a list of confirmed "Friends", as in, anyone who is willing to buy a gift for you or have a gift bought for them by you. You can check them out by going to Friends.
However, to help categorise people and to refine your wishlists (Let's face it, nobody wants Jim from the Office seeing what you want from your Wife on your anniversary) we've introduced Circles, which are essentially lists of friends that are related to a wishlist. It's a simple concept you may be familiar with. If you want to categorise your friends, we recommend you break them down into meaningful groups that are likely to attend the same events. Perhaps seperate family from your closest friends, and both of those again from your workplace. You can have as many or as few circles/wishlists as you want.</p>
<p>Adding friends is as simple as heading to one of your lists and hitting Add Friend. If they're signed up already it'll add them, if not, they'll be invited. </p>
<p>If you're already friends with someone you can tick their checkbox then hit Add Friends to add them to this list/circle.</p>
<p>Controlling your Circles is done in the list management area too. When you're on a list, click the garbage/bin icon to remove that friend from the list!</p>
<p>You have total control over who can see which lists, and if worst comes to worst, you can always delete someone from your friends list entirely by selecting them from your friends list, then hitting Delete.</p>

<h3>Q. Forget wishlists and friends - How can I manage my own profile?</h3>
<p>Simple! Click your own name at the top right of any page, then hit Edit My Profile. You can alter your email, password and your name.</p>

<h3>Q. I hate receiving emails, can I stop getting emailed every time something happens to a list?</h3>
<p><b>A.</b> Yes! Just navigate to the list through the dashboard and hit Unsubscribe. Alternatively, if you buy something from the list we'll assume you don't want any more emails from that list unless it's directly related to what you've bought for someone.</p>
<p>Equally, if you are extremely interested in a list and other gifts on it, you can always re-subscribe to the emails from a list by returning to it after you've bought something and hitting that button.</p>

<h3>Q. What happens when lists are edited?</h3>
<p><b>A.</b> If you mess with your list after a friend has reserved or bought something, we drop them an email to let them know. Equally, if something you're getting is edited, we send you an email, even if you're unsubscribed. No nasty surprises on the big day.</p>

<h3>Q. Can I edit the details of a list, such as its name and expiry date?</h3>
<p><b>A.</b> Yep! Just head to a list then click "Edit List Details".</p>

<h3>Q. I made a big mistake; I want to delete a list completely.</h3>
<p><b>A.</b> Head to a list, click "Edit List Details" and then "Delete this list". Make sure you're sure though!</p>

<h3>Q. What's this bookmarklet or browser button business about?</h3> 
<p><b>A.</b> Good question!</p>
<p>When you go to add gifts to your list, you can optionally decide to add a button to your bookmarks toolbar in your browser by clicking "Get the Browser Button". This streamlines the gift adding process by
letting you click the button when you're on any other site and automatically add what you're looking at to one of your lists.
Using this method you can still browse your favourite online stores in your usual way while creating your lists. It's the efficient, smooth way of using Gift Circle and we think it's pretty awesome.</p>
<p>Check it out here.</p>

<h3>Q. I don't see any bookmark toolbar to add the button to! Help!</h3>
<p><b>A.</b> Not to worry. Different browsers handle this in different ways, so here's a handy guide to the most popular browsers and how to make them show the Bookmarks (or Favorites) Toolbar.</p>

<p><b>Firefox:</b> Right click on an open area of the Firefox menu near the top of your screen, then check  Bookmarks Toolbar . Alternatively, Press Alt, then V, then T, then B. </p>
<p><b>Chrome:</b> Click on the wrench icon in the top right, go to  Bookmarks , then check  Show Bookmarks Bar . Alternatively, press Ctrl + Shift + B (Windows) or Command + B (Mac) at the same time. </p>
<p><b>Safari:</b> Click on the gear icon in the top right, and tick  Show Bookmarks Toolbar . Alternatively, Press Command + B (Mac) or Ctrl + Shift + B (Windows) at the same time. </p>
<p><b>IE9:</b> Right click on an open area of the Internet Explorer menu near the top of your screen, then check  Favourites Bar . Alternatively, Press Alt, click View, then Toolbars. Now click Favorites bar </p>
<p><b>IE7 or IE8:</b> Click Tools, then Toolbars. Now click Favourites Bar. </p>




